const fs = require("fs")
const path = require("path")

const projectRoot = path.resolve(__dirname, "..")
const templateDir = path.join(projectRoot, "template")
const outputDir = path.join(projectRoot, "src", "scripts", "generated")
const outputFile = path.join(outputDir, "staticTemplateManifest.ts")

function toPosixPath(filePath) {
	return filePath.replace(/\\/g, "/")
}

function buildManifestSource(templateNames) {
	const entries = templateNames
		.map((name) => {
			const relativeUrl = toPosixPath(path.join("../../../template", name))
			return `\t{\n\t\tname: ${JSON.stringify(name)},\n\t\turl: new URL(${JSON.stringify(relativeUrl)}, import.meta.url).toString(),\n\t},`
		})
		.join("\n")

	return `export type StaticTemplateEntry = {
\tname: string
\turl: string
}

// Generated by scripts/generate-template-manifest.js
export const staticTemplateManifest: StaticTemplateEntry[] = [
${entries}
]
`
}

function main() {
	const templateNames = fs
		.readdirSync(templateDir, { withFileTypes: true })
		.filter((entry) => entry.isFile() && entry.name.endsWith(".tex"))
		.map((entry) => entry.name)
		.sort((a, b) => a.localeCompare(b))

	fs.mkdirSync(outputDir, { recursive: true })
	fs.writeFileSync(outputFile, buildManifestSource(templateNames), "utf8")
	console.log(`[manifest] wrote ${templateNames.length} templates to ${path.relative(projectRoot, outputFile)}`)
}

main()
